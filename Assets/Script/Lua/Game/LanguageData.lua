function LuaLanguageData.ctor()
	this.gLanguageData=this
end
function LuaLanguageData.OnLoadLanguageComplete(obj)
	local urlXml=obj.xml
	if (urlXml==nil) then
		return 
	end
	this.setData(urlXml)
end
function LuaLanguageData.setData(xml)
	local i=0
	i
	while  i<String(xml.data.language).length()  do
		if (this.hasOwnProperty(xml.data.language[i].@id)) then
			if (nil==this[xml.data.language[i].@id]) then
				continue
			end
			this[xml.data.language[i].@id]=xml.data.language[i].@str
		end
				i=i+1
	end
end
